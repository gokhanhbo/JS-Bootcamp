//The == Equality Operator
console.log(1 == 1);
console.log(1 == "1");
console.log("z" == "z");
console.log(0 == "");
console.log(undefined == null);
console.log(false == 0);
console.log(true == 1);

console.log("-----------------")

// The === Strict Equality Operator
console.log(1 === 1);
console.log(1 === "1");
console.log("z" === "z");
console.log(0 === "");
console.log(undefined === null);
console.log(false === 0);
console.log(true === 1);

console.log("-----------------")

// The != Non-Equality Operator (-)
console.log(1 != 1);
console.log(1 != "1");
console.log("z" != "z");
console.log(false != true);
console.log(0 != "");
console.log(undefined != null);
console.log(false != 0);
console.log(true != 1);

console.log("-----------------")

// The Strict Non-Equality Operator (-)
console.log(1 !== 1);
console.log(1 !== "1");
console.log("z" !== "z");
console.log(false !== true);
console.log(0 !== "");
console.log(undefined !== null);
console.log(false !== 0);
console.log(true !== 1);

